package L1_Oct22;

public class VariableDemo {

	public static void main(String[] args) {

		// declaration
//		 int a;
//		 int b;

		// initialization
		// a = 10;
		// b = 20;
		
		// declare & initialize
		int a = 10 ;
		int b = 20 ;
		
		// updating the value
		a = 50 ;
		
		
		System.out.println(51 / 5); // quotient
		System.out.println(51 % 5); // remainder
		
		System.out.println(a == 50); // true
		System.out.println(a == 60); // false
		
		System.out.println(a >= 70 && b <= 50);
		System.out.println(a >= 70 || b <= 50);
		
		
		
	}

}
